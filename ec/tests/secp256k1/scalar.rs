use ec::secp256k1::scalar::*;

#[test]
fn convert_b32() {
    let exp_bytes = [
        0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0,
        0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0,
    ];
    let norm_bytes = [
        0x01, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x02, 0x00, 0x00,
        0x00, 0x1a, 0x23, 0x51, 0x45, 0xc5, 0x5f, 0xb7, 0x50, 0x74, 0xa1, 0x2d, 0x40, 0xc0, 0xbe,
        0xc9, 0x2f,
    ];
    let mut s = Scalar::from([1; 8]);
    assert_eq!(s.get_b32(), exp_bytes);

    s.set_b32(&exp_bytes).unwrap();
    assert_eq!(s.get_b32(), norm_bytes);
}
